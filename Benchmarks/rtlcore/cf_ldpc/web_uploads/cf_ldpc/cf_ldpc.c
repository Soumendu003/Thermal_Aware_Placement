static FILE* sim_file;
static int sim_count;
static unsigned char sim_start_i[1];
static unsigned char sim_message_i[3];
static unsigned char sim_valid_o[1];
static unsigned char sim_message_o[3];

void cf_ldpc_sim_init(const char* file) {
  time_t date = time(NULL);
  sim_count = 1;
  cf_ldpc_init();
  sim_start_i[0] = start_i[0];
  sim_message_i[0] = message_i[0];
  sim_message_i[1] = message_i[1];
  sim_message_i[2] = message_i[2];
  sim_valid_o[0] = valid_o[0];
  sim_message_o[0] = message_o[0];
  sim_message_o[1] = message_o[1];
  sim_message_o[2] = message_o[2];
  sim_file = fopen(file, "w");
  fprintf(sim_file,
    "$date\n  %s$end\n"
    "$version\n  Confluence 0.6.3 -- Launchbird Design Systems, Inc.\n$end\n"
    "$timescale\n  1 ns\n$end\n"
    "$scope module cf_ldpc $end\n"
    "$var wire 1 ! start_i $end\n"
    "$var wire 20 \" message_i $end\n"
    "$var wire 1 # valid_o $end\n"
    "$var wire 20 $ message_o $end\n"
    "$upscope $end\n"
    "$enddefinitions $end\n"
    "#0\n"
    "$dumpvars\n", asctime(localtime(&date)));
  fprintf(sim_file, start_i[0] ? "1!\n" : "0!\n");
  {
    int bit;
    int byte;
    int found = 0;
    for (byte = 2; byte >= 0; byte--) {
      for (bit = 128; bit >= 1; bit /= 2) {
        if (found)
          fprintf(sim_file, (bit & message_i[byte]) ? "1" : "0");
        else if (bit & message_i[byte]) {
          found = 1;
          fprintf(sim_file, "b1");
        }
      }
    }
    if (!found)
      fprintf(sim_file, "b0");
    fprintf(sim_file, " \"\n");
  }
  fprintf(sim_file, valid_o[0] ? "1#\n" : "0#\n");
  {
    int bit;
    int byte;
    int found = 0;
    for (byte = 2; byte >= 0; byte--) {
      for (bit = 128; bit >= 1; bit /= 2) {
        if (found)
          fprintf(sim_file, (bit & message_o[byte]) ? "1" : "0");
        else if (bit & message_o[byte]) {
          found = 1;
          fprintf(sim_file, "b1");
        }
      }
    }
    if (!found)
      fprintf(sim_file, "b0");
    fprintf(sim_file, " $\n");
  }
  fprintf(sim_file, "$end\n");
}

void cf_ldpc_sim_end() {
  fprintf(sim_file, "#%d\n", sim_count);
  fclose(sim_file);
}

